#include "3-cal.h"

/**
 * op_add - Entry point for addition
 * @a: var integer
 * @b: var integer
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Entry point for subtraction
 * @a: var integer
 * @b: var integer
 * Return: difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication entry point
 * @a: var integer
 * @b: var integer
 * Return: product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division entry point
 * @a: var integer
 * @b: var integer
 * Return: quotient of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus entry point
 * @a: var integer
 * @b: var integer
 * Return: remainder of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
